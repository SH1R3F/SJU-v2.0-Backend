     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        if ($request->perPage) {
          $categories = BlogCategory::filter($request)->sortData($request)->offset($request->perPage * $request->page)->paginate($request->perPage);
        } else {
          $categories = BlogCategory::filter($request)->sortData($request)->get();
        }
        return response()->json([
          'total'      => BlogCategory::filter($request)->count(),
          'categories' => $categories,
        ]);
    }

